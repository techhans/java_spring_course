let array1 = ['사과','토마토','오렌지'];
let array2 = ['apple','tomato','orange'];

array1.sort();
console.log(array1);  // [ '사과', '오렌지', '토마토' ]
array2.sort();
console.log(array2);  // [ 'apple', 'orange', 'tomato' ]

array1.reverse();
console.log(array1);  // [ '토마토', '오렌지', '사과' ]
array2.reverse();
console.log(array2);  // [ 'tomato', 'orange', 'apple' ]



